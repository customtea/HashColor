colorlist=[]

for c in OldColor:
    colorlist.append(c.value)



def colordot(hexstring):
    width = 640
    height = 640
    color_img = np.zeros((height,width,3), dtype=np.uint8)
    cell_width = 80
    cell_height = 80
    i = 0

    for h in range(0, height, cell_height):
        for w in range(0, width, cell_width):
            x = int(hexstring[i],16)
            color_img[h:h+cell_height,w:w+cell_width]=colorlist[x]
            i+=1
    cv2.imwrite("square.bmp",color_img)


